def get_ba_yi_fate(number):
    """
    根据数字查询81数理吉凶
    """
    if number <= 0:
        return "凶", "数理不详，需重新核对。"
    # 81 数理循环
    mod = number % 81
    if mod == 0:
        mod = 81
    return BA_YI_TABLE.get(mod, ("平", "此数理较为平稳，宜修身养性。"))


def calculate_wuge(surname, given_name):
    """
    计算姓名五格
    surname: 姓
    given_name: 名（可以是一个或两个字）
    返回值：字典，包含五格和每个格的解析
    """
    surname_strokes = []
    for char in surname:
        count = get_stroke_count(char)
        if count is None:
            raise ValueError(f"无法识别汉字 '{char}' 的笔画数")
        surname_strokes.append(count)

    given_name_strokes = []
    for char in given_name:
        count = get_stroke_count(char)
        if count is None:
            raise ValueError(f"无法识别汉字 '{char}' 的笔画数")
        given_name_strokes.append(count)

    # 天格：单姓 = 姓笔画 + 1；复姓 = 姓两字笔画之和
    if len(surname_strokes) == 1:
        tiange = surname_strokes[0] + 1
    else:
        tiange = sum(surname_strokes)

    # 人格：姓最后一字 + 名第一字
    if len(surname_strokes) >= 1 and len(given_name_strokes) >= 1:
        renge = surname_strokes[-1] + given_name_strokes[0]
    else:
        renge = sum(surname_strokes) + sum(given_name_strokes)

    # 地格：名笔画之和；单名 = 名笔画 + 1
    if len(given_name_strokes) == 1:
        dige = given_name_strokes[0] + 1
    else:
        dige = sum(given_name_strokes)

    # 总格：姓名全部笔画之和
    zongge = sum(surname_strokes) + sum(given_name_strokes)

    # 外格：总格 - 人格 + 1
    waige = zongge - renge + 1

    result = {
        "tiange": {"value": tiange, "fate": get_ba_yi_fate(tiange)},
        "renge": {"value": renge, "fate": get_ba_yi_fate(renge)},
        "dige": {"value": dige, "fate": get_ba_yi_fate(dige)},
        "waige": {"value": waige, "fate": get_ba_yi_fate(waige)},
        "zongge": {"value": zongge, "fate": get_ba_yi_fate(zongge)},
    }

    return result


def get_wuge_summary(wuge_result):
    """
    获取五格综合简评
    """
    ge_names = {
        "tiange": "天格",
        "renge": "人格",
        "dige": "地格",
        "waige": "外格",
        "zongge": "总格",
    }

    summary = []
    for key, name in ge_names.items():
        value = wuge_result[key]["value"]
        fate, desc = wuge_result[key]["fate"]
        summary.append(f"{name} {value} 数：{fate}。{desc}")

    return "\n".join(summary)


