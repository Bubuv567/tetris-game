def get_hexagram_by_number(number):
    """根据卦序号获取卦象信息"""
    number = ((number - 1) % 64) + 1
    return HEXAGRAMS.get(number)


def generate_hexagram_from_info(name, year, month, day, hour):
    """
    根据姓名和出生信息生成一个易经卦象
    使用简单的哈希算法让结果确定但看起来有依据
    """
    # 计算一个种子数
    seed = sum(ord(c) for c in name)
    seed += year * 366 + month * 31 + day + hour

    # 生成卦序号 1-64
    hexagram_number = (seed % 64) + 1
    return get_hexagram_by_number(hexagram_number)


